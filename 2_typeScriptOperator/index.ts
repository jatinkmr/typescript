// 1) Arithmetic Operators
// addition
let a1 = 20;
let b1 = 30;
let c1 = a1 + b1;
console.log('Addition => ', c1);

// subtraction
let a2 = 30;
let b2 = 20;
let c2 = a2 - b2;
console.log('Subtraction => ', c2);

// Multiplication
let a3 = 30;
let b3 = 20;
let c3 = a3 * b3;
console.log('Multiplication => ', c3);

// Division
let a4 = 100;
let b4 = 20;
let c4 = a4 / b4;
console.log('Division => ', c4);

// Modulus
let a5 = 95;
let b5 = 20;
let c5 = a5 % b5;
console.log('Modulus => ', c5);

// Increment
let a6 = 55;
a6++;
console.log('Increment => ', a6);

// Decrement
let a7 = 55;
a7--;
console.log('Decrement => ', a7);

// 2) Comparison or Relation Operators
// is equal to ==
let a8 = 10;
let b8 = 20;
console.log('a8 == b8 => ', a8 == b8);
console.log('a8 == 10 => ', a8 == 10);

// identical (===)
console.log('a8 === b8 => ', a8 ===  b8);
console.log('a8 === 10 => ', a8 === 10);

// Not equal to !=
console.log('a8 != b8 => ', a8 != b8);
console.log('a8 != 10 => ', a8 != 10);

// Not Identical !==
console.log('a8 !== b8 => ', a8 !== b8);
console.log('a8 !== 10 => ', a8 !== 10);

// Greater Than
console.log('a8 > b8 => ', a8 > b8);
console.log('a8 > 30 => ', a8 > 30);

// Greater Than Equal To
console.log('a8 >= b8 => ', a8 >= b8);
console.log('a8 >=30 => ', a8 >=30);

// Less Than
console.log('a8 < b8 => ', a8 < b8);
console.log('a8 < 10 => ', a8 < 10);

// Less Than Equal To
console.log('a8 <= b8 => ', a8 <= b8);
console.log('a8 <= 10 => ', a8 <= 10);


